axis.NewsFeedItem = Backbone.View.extend({
  tagName: 'article',
  className: 'row',

  events: {
    'click a' : 'open'
  },

  initialize: function() {
    this.template = _.template($('#news-feed-story-template').html());
    this.externalTemplate = _.template($('#external-news-feed-story-template').html());
  },

  render: function() {
    if (this.model.get('external') === true) {
      this.$el.html(this.externalTemplate(this.model.toJSON()));
    } else {
      this.$el.html(this.template(this.model.toJSON()));
    }
    return this;
  },

  open: function(event) {
    if (event.target.className !== 'external') {
      event.preventDefault();
    
      $newsview = $(document).find('#news-container');
      $newsview.children().hide();
      $newsview.prepend(
        new axis.ArticleContainer({model: this.model}).render().el
      );

      axis.router.navigate('/article/' + this.model.get('slug') + '/');

      // Scroll to headline
      $('body').scrollTop($("header h2").offset().top - 50);

      // Init Affix
      $('.moving-container').affix({offset: { top: 70 } });
    }
  }
});

