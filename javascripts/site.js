axis.Models.Article = Backbone.Model.extend({
  initialize: function() {
    this.setTags();
  },

  setTags: function() {
    _.each(this.get('tags'), function(tag) {
      this.set(tag, true);
    }, this);
  }
});

axis.Collections.Articles = Backbone.Collection.extend({
  model: axis.Models.Article,

  getBySlug: function(slug) {
    return this.find(function(article) {
      return article.get('slug') === slug;
    });
  },

  getMostRecent: function(count) {
    return [this.last()];
    //sort by publish date
    //grab the number of stories specified by count
  }
});

axis.Views.NewsContainer = Backbone.View.extend({
  events : {
    'click .close': 'refresh',
    'click .project-section-nav': 'syncTabs',
    'click .project-section-nav a': 'goToTab'
  },

  goToTab: function() {
    //$('body').animate({ scrollTop: $(".tabs-content").offset().top - 125 }, 800);
  },

  syncTabs: function(event) {
    var activeBar = event.currentTarget.classList[1],
        activeTab = event.target.hash;
        inactiveBar = activeBar === 'large' ? 'small' : 'large';

    $('.project-section-nav.' + inactiveBar + ' .active').removeClass('active');
    $('.project-section-nav.' + inactiveBar + ' dd a[href="' + activeTab + '"]').parent().addClass('active');
  },

  refresh: function() {
    this.$el.find('#story').remove();
    this.$el.find('#sidebar').remove();
    
    this.$el.children().show();
    
    if(window.innerWidth > 767) {
      this.$el.find('.project-section-nav.small').hide();
      this.$el.find('.project-section-nav.large').show();
    } else {
      this.$el.find('.project-section-nav.small').show();
      this.$el.find('.project-section-nav.large').hide();
    }

    axis.router.navigate('demo/project/avi/');
  }
});

axis.Views.Features = Backbone.View.extend({
  initialize: function() {
    features = axis.articles.filter(function(article){
      return article.get('feature') === true;
    }).map(function(story){
      return (new axis.Views.FeatureItem({model: story}).render().el);
    });

    this.$el.find('#featured').html(features);

    $(window).load(function() {
      $('#featured').orbit({ timer: 'true' });
    });
  }
});

