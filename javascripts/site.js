axis.Views.ArticleNavigation = Backbone.View.extend({
  tagName: 'ul',

  initialize: function() {
    if (this.options.prev) {
      this.previous = new axis.Views.ArticleNavigationItem({
        model: this.options.prev,
        className: 'previous six mobile-two columns',
        direction: 'Previous'
      }).render().el;
    } else {
      this.previous = '<li class="previous six columns"><i class="none">You are at the oldest article</i></li>';
    }

    if (this.options.next) {
      this.next = new axis.Views.ArticleNavigationItem({
        model: this.options.next,
        className: 'next six mobile-two columns',
        direction: 'Next'
      }).render().el;
    } else {
      this.next = '<li class="next six columns"><i class="none">You are at the most recent story</i></li>';
    }
  },

  render: function() {
    this.$el.append(this.previous, this.next);
    return this;
  }
});

